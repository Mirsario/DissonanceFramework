namespace Dissonance.Framework.OpenAL
{
	/// <summary> 8-bit boolean AL.Source/AL.GetSource parameters. </summary>
	public enum SourceBool
	{
		/// <summary> Indicate that the Source has relative coordinates. </summary>
		SourceRelative = 0x202,
		/// <summary>  Indicate whether or not the source should be looping. Default: False.  </summary>
		Looping = 0x1007,
	}
}
